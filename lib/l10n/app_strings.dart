  /// Resolves [key] and substitutes `{name}` placeholders with [params] values.
  String f(String key, Map<String, String> params) {
    var value = t(key);
    params.forEach((name, replacement) {
      value = value.replaceAll('{$name}', replacement);
    });
    return value;
  }
